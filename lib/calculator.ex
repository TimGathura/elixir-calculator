# All calculator functions will come here

# Main module containing all the calculator functions
defmodule Calculator do

    # 1. Addition function
    def add(a,b) do
        a + b
    end

    # 2. Subtraction function
    def subtract(a,b) do
        a - b
    end

    # 3. Multiplication function
    def mult(a,b) do
        a * b
    end

    # 4. Division function
    def divide(a,b) do
      a / b
    end





end
